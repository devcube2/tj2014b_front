/*
    JS 의 데이터 기본 타입
        1. 문자열(String)   : " " 또는 ' ' 또는 ``(백틱-키보드TAB) 으로 감싼 자료
            10 vs "10" : 자료는 10 같다. 자료의 타입은 다르다.
            예 ] '안녕하세요' , "유재석" , `<div> </div>`
        2. 숫자(Number)
            예 ] 10 , 7 , 3.14
        3. 불리언(Boolean)
            예 ] true 또는 false
*/

10          // 숫자 타입 의 자료
"안녕하세요" // 문자열 타입 의 자료
true        // 불리언 타입 의 자료